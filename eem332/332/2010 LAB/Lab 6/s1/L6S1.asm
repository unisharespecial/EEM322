.MODEL SMALL
.STACK 100h
.DATA 
	MAXCUMLE EQU 100
	STRING DB MAXCUMLE DUP(?)
	COUNT DB  (?)
.CODE
START: 
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX
	MOV AH,3Fh
	MOV BX,01h
	MOV CX, MAXCUMLE
	MOV DX,OFFSET STRING
	INT 21H
	AND AX,AX
	JZ DONE
	MOV CX,AX
	MOV DI,OFFSET STRING
    MOV AL,' '
	MOV AH,CL 
KONTROL:
    CMP AL,[DI]
    JZ SAY
	JNZ NO
SAY:
    INC BX
	CMP [DI+1],AL
    JZ ATLA
	
	
ATLA: 
	INC DI
	INC DX
	CMP AL,[DI]
	JZ ATLA 
NO:
	INC DI
	INC DX
	CMP ah,Dl
	JZ ATLA
	
	LOOP KONTROL   	

	MOV AX,BX
	MOV DX,AX
	OR DX,3030H
	INT 21h
DONE:
		MOV AH,4CH
		INT 21H
ENDP
END START		
