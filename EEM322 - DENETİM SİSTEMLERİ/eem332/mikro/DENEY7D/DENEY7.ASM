		LIST P=16F84A
STATUS  EQU	H'03'
PORTB 	EQU H'06'
PORTA	EQU	H'05'
TRISB	EQU H'86'
TRISA	EQU	H'85'
		ORG 0X00
		CLRF PORTB
		BSF STATUS,5
		CLRF TRISB
		MOVLW H'FF'
		MOVWF TRISA
		BCF STATUS,5
TEKRAR
		BTFSS PORTA,0
		GOTO GIT
		MOVLW B'00001111'
		MOVWF PORTB
		GOTO TEKRAR
GIT
		MOVLW B'11110000'
		MOVWF PORTB
		GOTO TEKRAR
		END